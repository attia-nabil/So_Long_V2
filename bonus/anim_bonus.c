#include "so_long_bonus.h"

int	ft_animation(t_variable *so)
{
	if (so->p[so->i][so->j] == 'C' && so->p[so->i][so->j + 1] != '1'
			&& so->p[so->i][so->j + 1] != 'E'
			&& so->p[so->i][so->j + 1] != 'P' && so->p[so->i][so->j + 1] != 'N')
	{
		so->p[so->i][so->j + 1] = 'C';
		so->p[so->i][so->j] = '0';
	}
	else if (so->p[1][so->j] == 'C' && so->p[1][so->j - 1] != '1'
			&& so->p[1][so->j - 1] != 'E'
			&& so->p[1][so->j - 1] != 'P' && so->p[1][so->j - 1] != 'N')
	{
		so->p[1][so->j - 1] = 'C';
		so->p[1][so->j] = '0';
	}
	return (0);
}
