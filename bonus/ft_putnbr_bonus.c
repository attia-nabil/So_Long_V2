#include "so_long_bonus.h"

void	ft_putnbr(int n)
{
	long	n1;

	n1 = n;
	if (n1 >= 0 && n1 <= 9)
	{
		ft_putchar(n1 + 48);
	}
	if (n1 >= 10)
	{
		ft_putnbr(n1 / 10);
		ft_putchar(n1 % 10 + 48);
	}
}
