#include "so_long.h"

int	calcule_l(char **p)
{
	int	i;

	i = 0;
	while (p[0][i])
	{
		i++;
	}
	return (i);
}


int	calcule_w(char **p)
{
	int	i;

	i = 0;
	while (p[i])
	{
		i++;
	}
	return (i);
}
